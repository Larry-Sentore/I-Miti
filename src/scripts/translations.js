// Returns the translated string for key in the current language, falling back to English.
window.t = function(key) {
  var lang = localStorage.getItem('imiti_lang') || 'en';
  var dict = window.TRANSLATIONS[lang] || window.TRANSLATIONS['en'];
  return (dict[key] !== undefined ? dict[key] : (window.TRANSLATIONS['en'][key] || key));
};

// Applies all translations to [data-i18n] and [data-i18n-ph] elements in the current DOM.
window.applyTranslations = function() {
  document.querySelectorAll('[data-i18n]').forEach(function(el) {
    el.textContent = window.t(el.getAttribute('data-i18n'));
  });
  document.querySelectorAll('[data-i18n-ph]').forEach(function(el) {
    el.placeholder = window.t(el.getAttribute('data-i18n-ph'));
  });
  var btn = document.getElementById('lang-toggle');
  if (btn) btn.textContent = window.t('lang_toggle_label');
};

window.setLanguage = function(lang) {
  localStorage.setItem('imiti_lang', lang);
  window.applyTranslations();
};

window.toggleLanguage = function() {
  var current = localStorage.getItem('imiti_lang') || 'en';
  window.setLanguage(current === 'en' ? 'rn' : 'en');
};

